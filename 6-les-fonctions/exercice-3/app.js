// Exercice 3 : Longueur du mot

// Créez une fonction qui prend un mot en paramètre et retourne la longueur de ce mot. Affichez le résultat pour plusieurs mots différents.

function longueurMot(mot) {
    return mot.length
}
  
const resultat = longueurMot("voiture")
console.log(resultat);

console.log(longueurMot("lamborghini"));

console.log(longueurMot("route"));